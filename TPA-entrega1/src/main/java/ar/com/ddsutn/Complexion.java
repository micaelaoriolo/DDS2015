package ar.com.ddsutn;

public class Complexion {
	
	private int altura;
	private int peso;
	
	public Complexion(int altura, int peso) {
		//super();
		this.altura = altura;
		this.peso = peso;
	}
	public int getAltura() {
		return altura;
	}
	public void setAltura(int altura) {
		this.altura = altura;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	

}
